// Cumulative sums: CUMSUM[i] = total verses in surahs 1..i
// CUMSUM[0] = 0, CUMSUM[1] = 7 (just Al-Fatihah), CUMSUM[2] = 293 (Fatihah + Baqarah)
export const CUMSUM: number[] = (() => {
  const out = [0];
  for (let i = 0; i < SURAH_VERSE_COUNTS.length; i++) {
    out.push(out[i] + SURAH_VERSE_COUNTS[i]);
  }
  return out;
})();

export const TOTAL_AYAT = CUMSUM[114]; // 6236

/** Convert absolute ayah number (1..6236) → { surah, ayah } (both 1-indexed). */
export function absoluteToSurahAyah(absolute: number): { surah: number; ayah: number } {
  if (absolute < 1 || absolute > TOTAL_AYAT) {
    throw new Error(`Absolute ayah ${absolute} out of range`);
  }
  // Binary search for the largest i where CUMSUM[i] < absolute
  let lo = 0;
  let hi = 114;
  while (lo < hi) {
    const mid = (lo + hi) >> 1;
    if (CUMSUM[mid] >= absolute) hi = mid;
    else lo = mid + 1;
  }
  // lo is the surah number (1-indexed)
  const surah = lo;
  const ayah = absolute - CUMSUM[surah - 1];
  return { surah, ayah };
}

/** Convert surah:ayah → absolute ayah number. */
export function surahAyahToAbsolute(surah: number, ayah: number): number {
  return CUMSUM[surah - 1] + ayah;
}

export function makeKey(surah: number, ayah: number): string {
  return `${surah}:${ayah}`;
}

