export function getParahForVerse(surah: number, ayah: number): number {
  const abs = surahAyahToAbsolute(surah, ayah);
  for (let p = 0; p < PARAH_RANGES.length; p++) {
    const r = PARAH_RANGES[p];
    const startAbs = surahAyahToAbsolute(r.start[0], r.start[1]);
    const endAbs = surahAyahToAbsolute(r.end[0], r.end[1]);
    if (abs >= startAbs && abs <= endAbs) return p + 1;
  }
  return 30;
}
